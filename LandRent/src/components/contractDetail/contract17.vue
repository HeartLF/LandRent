<div class="contract_sample_box"><p style="text-align:center"><strong><span style="font-size:24px">土地使用权抵押合同</span></strong></p>

<p style="text-align:center"><span style="font-size:20px">（一）</span></p>

<p><span style="font-size:18px">甲方(抵押人)：<br>
乙方(抵押权人)：</span></p>

<p>&nbsp; &nbsp; 根据《担保法》、《合同法》、《城市房地产管理法》和《城镇国有土地使用权出让和转让暂行条例》等法律法规的规定，甲、乙双方就土地使用权抵押有关事宜共同协商，达成一致意见，现签订合同如下：</p>

<p><span style="font-size:18px">一、抵押宗地基本情况</span><br>
&nbsp; &nbsp; 本宗地座落于<u> &nbsp; &nbsp; </u>镇(乡)<u> &nbsp; &nbsp; </u>村(集镇、社区)<u> &nbsp; &nbsp; &nbsp;</u>街<u> &nbsp; &nbsp; &nbsp;</u>号，甲方于<u> &nbsp; &nbsp;</u>年以<u> &nbsp; &nbsp; &nbsp; &nbsp;</u>方式取得<u> &nbsp; &nbsp; &nbsp; </u>土地使用权，土地使用证号为：<u> &nbsp; &nbsp; </u>公 <u>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</u>&nbsp;用(<u> &nbsp; &nbsp; &nbsp;</u>)字第<u> &nbsp; &nbsp; &nbsp;</u>号。登记土地使用权面积为<u> &nbsp; &nbsp; &nbsp;</u>m2，用途为<u> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</u>，使用年限为<u> &nbsp; &nbsp; &nbsp; </u>年，使用终止日期为<u> &nbsp; &nbsp; &nbsp; </u>年<u> &nbsp; &nbsp; </u>月<u> &nbsp; &nbsp; </u>日。本宗地上建筑物、构筑物占地面积为 &nbsp; &nbsp; &nbsp;m2，建筑面积为 &nbsp; &nbsp; &nbsp;m2。</p>

<p><span style="font-size:18px">二、抵押贷款情况</span><br>
&nbsp; &nbsp; 本宗地土地使用权评估价额为人民币<u> &nbsp; &nbsp; &nbsp; &nbsp;</u>元，抵押金额为人民币<u> &nbsp; &nbsp; &nbsp; &nbsp; </u>元，欠缴土地使用权出让金为人民币 <u>&nbsp; &nbsp; &nbsp; </u>元，抵押期限为 <u>&nbsp; &nbsp; </u>年，(抵押期限以抵押登记的批准之日起计算)。</p>

<p><span style="font-size:18px">三、双方权利和义务</span>：<br>
&nbsp; &nbsp; 1、抵押合同签订后15日内，甲、乙双方须共同申请土地使用权抵押设定登记，未经登记的土地使用权抵押权不受法律保护。<br>
&nbsp; &nbsp; 2、甲方在抵押期间有权继续占有、使用抵押物。<br>
&nbsp; &nbsp; 3、甲方在抵押期间应保护好抵押物，保证抵押物不受毁损。<br>
&nbsp; &nbsp; 4、甲方应在抵押期限内归还贷款本息。贷款本息归还后，甲、乙双方应共同申请土地使用权抵押权注销登记。</p>

<p><span style="font-size:18px">四、违约责任</span><br>
&nbsp; &nbsp; 1、甲方擅自将抵押物转让、出租或以其它方式处分时，其行为无效。乙方有权提前收回贷款，并可要求甲方支付抵押金额 %的违约金。<br>
&nbsp; &nbsp; 2、由甲方因隐瞒抵押物的共有、争议、查封、扣留或已经设定过抵押等情况所产生的后果，由甲方承担责任。<br>
&nbsp; &nbsp; 3、由甲方占管的抵押物发生毁损，导致土地价值减少甚至破坏，乙方可要求甲主恢复土地的原价值或赔偿损失。<br>
&nbsp; &nbsp; 4、抵押到期不能履行债务或者在抵押期间宣告解散、破产时，乙方有权依照法律规定处分抵押财产。处分抵押财产所得，由甲方交清土地使用权出让金后，乙方有优先受偿权。因处分抵押财产而取得土地使用权和地上建筑物、其他附着物所有权的，应当依照有关规定办理过户登记。</p>

<p><span style="font-size:18px">五、其它</span><br>
&nbsp; &nbsp; 1、土地使用权抵押时，其地上建筑物、构筑物随之抵押。<br>
&nbsp; &nbsp; 2、本合同自土地使用权抵押权设定登记批准之日起生效。<br>
&nbsp; &nbsp; 3、本合同一式三份，甲、乙双方各执一份，另一份用于办理土地使用权抵押权设定登记。<br>
&nbsp; &nbsp; 4、抵押双方当事人在履行合同过程中发生争议，不能协商解决时，按《合同法》处理。</p>

<p style="text-align:center"><span style="font-size:20px">（二）</span></p>

<p><span style="font-size:18px">抵 押 人(以下简称甲方):<br>
抵押权人(以下简称乙方):</span></p>

<p>&nbsp; &nbsp; 甲、乙双方根据《中华人民共和国城市房地产管理法》和《中华人民共和国担保法》的有关规定,本着自愿,互利,公平和诚实信用的原则,就土地使用权抵押事项进行充分协商,特订立以下合同以资共同遵守.</p>

<p>&nbsp; &nbsp; 一、甲方为清尝债务以合法拥有的座落在<u> &nbsp; &nbsp; </u>镇(乡)<u> &nbsp; &nbsp; </u>路(村)<u> &nbsp; &nbsp; </u>巷(组)<u> &nbsp; &nbsp; </u>号土地使用权作抵押,向乙方提供经济担保.其四至为:东至<u> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; </u>;西至<u> &nbsp; &nbsp; &nbsp; &nbsp;</u>;南至<u> &nbsp; &nbsp; &nbsp; &nbsp;</u>;北至<u> &nbsp; &nbsp; &nbsp; &nbsp;</u>;抵押土地面积 <u>&nbsp; &nbsp; &nbsp; &nbsp;</u>平方米,现用途为<u> &nbsp; &nbsp; &nbsp;</u>用地.地上建筑物 <u>&nbsp; &nbsp; </u>幢 <u>&nbsp; &nbsp; </u>平方米;地上附着物<u> &nbsp; &nbsp; &nbsp; </u>平方米.</p>

<p>&nbsp; &nbsp; 二、担保范围:<br>
&nbsp; &nbsp; 1、抵押借款的贷款本息.<br>
&nbsp; &nbsp; 2、处分抵押物所需费用.<br>
&nbsp; &nbsp; 3、甲,乙双方应缴纳的税费.<br>
&nbsp; &nbsp; 4、其它</p>

<p>&nbsp; &nbsp; 三、抵押期限及抵押额:<br>
&nbsp; &nbsp; 甲方以上述土地使用权抵押于乙方,期限<u> &nbsp; &nbsp; &nbsp; </u>年(月),时间从<u> &nbsp; &nbsp;</u>年<u> &nbsp; &nbsp;</u>月<u> &nbsp; &nbsp;</u>日至<u> &nbsp; &nbsp;</u>年<u> &nbsp; &nbsp;</u>月<u> &nbsp; &nbsp;</u>日止.<br>
&nbsp; &nbsp; 抵押地块经评估<u> &nbsp; &nbsp; &nbsp;</u>年<u> &nbsp; &nbsp; &nbsp;</u>月<u> &nbsp; &nbsp; &nbsp; </u>日土地使用权抵押出让总价为人民币 <u>&nbsp; &nbsp; </u>万元,大写<u> &nbsp; &nbsp;</u>千<u> &nbsp; &nbsp;</u>百 <u>&nbsp; &nbsp;</u>十<u> &nbsp; </u>万<u> &nbsp; </u>千<u> &nbsp; &nbsp;&nbsp;</u>百<u> &nbsp; </u>十<u> &nbsp; </u>元<u> &nbsp; </u>角 &nbsp; 分.乙方同意以土地使用权总地价的 %,折合人民币<u> &nbsp; &nbsp;</u>万元,大写<u> &nbsp; </u>千<u> &nbsp; </u>百<u> &nbsp; </u>十<u> &nbsp; </u>万<u> &nbsp; </u>千<u> &nbsp; </u>百<u> &nbsp; </u>十<u> &nbsp; </u>元<u> &nbsp; </u>角<u> &nbsp; </u>分,为最高额贷款付给甲方.</p>

<p>&nbsp; &nbsp; 四,民事责任:<br>
&nbsp; &nbsp; 1、甲方对产权的责任,抵押期间,甲方应维护抵押地块土地使用现状的完好.乙方有权按抵押合同约定,检查由甲方占管的抵押物使用状况.<br>
&nbsp; &nbsp; 2、甲方占管期间,未征得乙方书面同意,不得擅自将抵押的土地出租,变卖,赠与,调换,不得任意改变其用途和再次抵押.</p>

<p>&nbsp; &nbsp; 五、违约责任及争议处理办法.</p>

<p>&nbsp; &nbsp; 六、抵押物的处分:<br>
&nbsp; &nbsp; 本宗地土地使用权原以划拨方式取得,土地使用权抵押期满,抵押人不能履行债务,抵押权人占有或处分土地使用权时,处分土地使用权所得价款,双方一致同意首先向政府交纳抵押人与 国土资源局签订的《国有土地使用权出让合同》所规定应交的出让金处分抵押物的费用.</p>

<p>&nbsp; &nbsp; 七、合同的变更与解除:<br>
&nbsp; &nbsp; 1、抵押合同如发生变更,甲,乙双方应在变更之日起,向<u> &nbsp; &nbsp; &nbsp; &nbsp;</u>市国土资源局申请办理抵押变更登记.<br>
&nbsp; &nbsp; 2、抵押关系终止,甲、乙双方应在终止之日起二十日(港、澳、台地区或境外当事人在三个月)内,向<u> &nbsp; &nbsp; &nbsp; </u>市国土资源局申请注销抵押登记.</p>

